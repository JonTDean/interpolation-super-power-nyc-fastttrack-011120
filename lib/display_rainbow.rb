def display_rainbow(colors)
  
  print "R: #{colors[0]}\n"
  print "O: #{colors[1]}\n"
  print "Y: #{colors[2]}\n"
  print "G: #{colors[3]}\n"
  print "B: #{colors[4]}\n"
  print "I: #{colors[5]}\n"
  print "V: #{colors[6]}\n"
  
end

display_rainbow(['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])